import { Document, Packer, Paragraph, TextRun, HeadingLevel, Table, TableRow, TableCell, WidthType } from 'docx';

export function buildJsonExport(prd, sections) {
  return {
    exportFormat: 'pm-ai-prd-v1',
    exportedAt: new Date().toISOString(),
    title: prd.title,
    status: prd.status,
    sections
  };
}

function sectionContent(s) {
  if (typeof s.content === 'string') return s.content;
  if (Array.isArray(s.content)) return s.content.join('\n');
  if (typeof s.content === 'object' && s.content !== null) return JSON.stringify(s.content, null, 2);
  return '';
}

export function buildMarkdown(title, sections) {
  let md = `# ${title}\n\n`;
  md += `> Status: draft · Generated by PM AI Tool · ${new Date().toLocaleDateString()}\n\n---\n\n`;

  for (const s of sections || []) {
    const num = s.num ? `${s.num} ` : '';
    md += `## ${num}${s.title || 'Section'}\n\n`;

    if (s.confidence) {
      md += `> Confidence: ${s.confidence}\n\n`;
    }

    if (s.type === 'list' && Array.isArray(s.items)) {
      for (const item of s.items) {
        if (typeof item === 'string') {
          md += `- ${item}\n`;
        } else if (typeof item === 'object' && item !== null) {
          const label = item.label || item.title || item.name || '';
          const desc = item.description || item.content || item.detail || '';
          if (label && desc) {
            md += `- **${label}**: ${desc}\n`;
          } else {
            md += `- ${label || desc || JSON.stringify(item)}\n`;
          }
        }
      }
      md += '\n';
    } else if (s.type === 'table' && s.rows) {
      const headers = s.headers || [];
      if (headers.length > 0) {
        md += '| ' + headers.join(' | ') + ' |\n';
        md += '|' + headers.map(() => ' --- ').join('|') + '|\n';
      }
      for (const row of s.rows || []) {
        const cells = Array.isArray(row) ? row : Object.values(row);
        md += '| ' + cells.map(c => String(c ?? '').replace(/\|/g, '\\|').replace(/\n/g, ' ')).join(' | ') + ' |\n';
      }
      md += '\n';
    } else {
      const content = sectionContent(s);
      if (content) {
        md += `${content}\n\n`;
      }
    }
  }

  md += `---\n\n*PM AI Tool*\n`;
  return md;
}

function escapeHtml(s) {
  if (s == null) return '';
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function markdownToHtml(text) {
  if (!text) return '';
  let html = escapeHtml(text);
  html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
  html = html.replace(/\*(.+?)\*/g, '<em>$1</em>');
  html = html.replace(/`(.+?)`/g, '<code>$1</code>');

  const lines = html.split('\n');
  const result = [];
  let inList = false;
  let listType = 'ul';

  for (const line of lines) {
    const trimmed = line.trim();
    if (/^[-*•]\s/.test(trimmed)) {
      if (!inList) { result.push('<ul>'); inList = true; listType = 'ul'; }
      result.push(`<li>${trimmed.replace(/^[-*•]\s+/, '')}</li>`);
    } else if (/^\d+\.\s/.test(trimmed)) {
      if (!inList) { result.push('<ol>'); inList = true; listType = 'ol'; }
      result.push(`<li>${trimmed.replace(/^\d+\.\s+/, '')}</li>`);
    } else {
      if (inList) {
        result.push(listType === 'ol' ? '</ol>' : '</ul>');
        inList = false;
      }
      if (/^####\s/.test(trimmed)) {
        result.push(`<h5>${trimmed.replace(/^####\s+/, '')}</h5>`);
      } else if (/^###\s/.test(trimmed)) {
        result.push(`<h4>${trimmed.replace(/^###\s+/, '')}</h4>`);
      } else if (/^##\s/.test(trimmed)) {
        result.push(`<h3>${trimmed.replace(/^##\s+/, '')}</h3>`);
      } else if (/^#\s/.test(trimmed)) {
        result.push(`<h2>${trimmed.replace(/^#\s+/, '')}</h2>`);
      } else if (trimmed === '') {
        result.push('');
      } else {
        result.push(`<p>${trimmed}</p>`);
      }
    }
  }
  if (inList) result.push(listType === 'ol' ? '</ol>' : '</ul>');
  return result.join('\n');
}

function renderSectionContent(s) {
  const parts = [];

  if (s.confidence) {
    parts.push(`<div class="confidence"><span class="confidence-badge">${escapeHtml(s.confidence)}</span></div>`);
  }

  if (s.type === 'list' && Array.isArray(s.items)) {
    parts.push('<ul class="feature-list">');
    for (const item of s.items) {
      if (typeof item === 'string') {
        parts.push(`<li>${markdownToHtml(item)}</li>`);
      } else if (typeof item === 'object' && item !== null) {
        const label = item.label || item.title || item.name || item.id || '';
        const desc = item.description || item.content || item.detail || '';
        if (label && desc) {
          parts.push(`<li><strong>${escapeHtml(label)}</strong>: ${markdownToHtml(desc)}</li>`);
        } else {
          parts.push(`<li>${markdownToHtml(label || desc || JSON.stringify(item))}</li>`);
        }
      }
    }
    parts.push('</ul>');
  } else if (s.type === 'table' && s.rows) {
    const headers = s.headers || [];
    parts.push('<div class="table-wrap"><table>');
    if (headers.length > 0) {
      parts.push('<thead><tr>' + headers.map(h => `<th>${escapeHtml(h)}</th>`).join('') + '</tr></thead>');
    }
    parts.push('<tbody>');
    for (const row of s.rows || []) {
      const cells = Array.isArray(row) ? row : Object.values(row);
      parts.push('<tr>' + cells.map(c => `<td>${escapeHtml(String(c ?? ''))}</td>`).join('') + '</tr>');
    }
    parts.push('</tbody></table></div>');
  } else {
    const content = sectionContent(s);
    if (content) {
      parts.push(markdownToHtml(content));
    }
  }

  return parts.join('\n');
}

