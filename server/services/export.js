export async function buildDocxBuffer(title, sections) {
  const children = [
    new Paragraph({
      text: title,
      heading: HeadingLevel.HEADING_1,
      spacing: { after: 200 },
    }),
    new Paragraph({
      children: [
        new TextRun({ text: `Status: draft · Generated by PM AI Tool · ${new Date().toLocaleDateString()}`, italics: true, size: 20, color: '666666' }),
      ],
      spacing: { after: 400 },
    }),
  ];

  for (const s of sections || []) {
    const num = s.num ? `${s.num} ` : '';
    children.push(
      new Paragraph({
        text: `${num}${s.title || 'Section'}`.trim(),
        heading: HeadingLevel.HEADING_2,
        spacing: { before: 300, after: 120 },
      })
    );

    if (s.confidence) {
      children.push(
        new Paragraph({
          children: [new TextRun({ text: `Confidence: ${s.confidence}`, italics: true, size: 18, color: '2E7D32' })],
          spacing: { after: 80 },
        })
      );
    }

    if (s.type === 'list' && Array.isArray(s.items)) {
      for (const item of s.items) {
        let text = '';
        if (typeof item === 'string') {
          text = item;
        } else if (typeof item === 'object' && item !== null) {
          const label = item.label || item.title || item.name || '';
          const desc = item.description || item.content || item.detail || '';
          text = label && desc ? `${label}: ${desc}` : (label || desc || JSON.stringify(item));
        }
        children.push(
          new Paragraph({
            children: [new TextRun({ text: `• ${text}` })],
            spacing: { after: 60 },
            indent: { left: 360 },
          })
        );
      }
    } else if (s.type === 'table' && s.rows && s.headers) {
      const headers = s.headers || [];
      const tableRows = [];
      if (headers.length > 0) {
        tableRows.push(
          new TableRow({
            children: headers.map(h =>
              new TableCell({
                children: [new Paragraph({ children: [new TextRun({ text: String(h), bold: true, size: 18 })] })],
                width: { size: Math.floor(9000 / headers.length), type: WidthType.DXA },
                shading: { fill: 'F0F0F8' },
              })
            ),
          })
        );
      }
      for (const row of s.rows || []) {
        const cells = Array.isArray(row) ? row : Object.values(row);
        tableRows.push(
          new TableRow({
            children: cells.map(c =>
              new TableCell({
                children: [new Paragraph({ children: [new TextRun({ text: String(c ?? ''), size: 18 })] })],
                width: { size: Math.floor(9000 / Math.max(cells.length, 1)), type: WidthType.DXA },
              })
            ),
          })
        );
      }
      if (tableRows.length > 0) {
        children.push(
          new Table({
            rows: tableRows,
            width: { size: 9000, type: WidthType.DXA },
          })
        );
        children.push(new Paragraph({ spacing: { after: 120 } }));
      }
    } else {
      const content = sectionContent(s);
      if (content) {
        const paras = content.split('\n').filter(l => l.trim());
        for (const p of paras) {
          const runs = [];
          const parts = p.split(/(\*\*[^*]+\*\*)/g);
          for (const part of parts) {
            if (part.startsWith('**') && part.endsWith('**')) {
              runs.push(new TextRun({ text: part.slice(2, -2), bold: true }));
            } else {
              runs.push(new TextRun(part));
            }
          }
          children.push(
            new Paragraph({ children: runs, spacing: { after: 80 } })
          );
        }
      }
    }
  }

  const doc = new Document({ sections: [{ children }] });
  return Packer.toBuffer(doc);
}
